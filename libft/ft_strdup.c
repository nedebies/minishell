#include "libft.h"

char	*ft_strdup(char const *src)
{
	char	*ptr;
	size_t	i;

	i = ft_strlen(src);
	ptr = malloc(sizeof(char) * (i + 1));
	if (ptr == NULL || src == NULL)
		return (0);
	i = 0;
	while (src[i])
	{
		ptr[i] = src[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
